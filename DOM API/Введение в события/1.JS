// Объект события

/*
С каждым возникающим событием связана информация, зависящая от типа события. Например событие click 
это не только факт сам по себе, но так же и координаты точки на экране, где был совершен клик. 
Эта информация доступна через специальный объект-событие.
*/

<div id="myElement">Бум!</div>
const button = document.getElementById('myElement');

button.addEventListener('click', e => alert(e.target.textContent));

/*
По умолчанию этот объект передается всегда, в любой обработчик, как единственный параметр. Если он вам не 
нужен, то его можно проигнорировать.

Базовые свойства объекта Event:

event.target - элемент на котором произошло событие

event.type - тип события
*/
// У каждого типа событий свой набор свойств, подробнее о них смотрите в документации.
// (https://developer.mozilla.org/en/docs/Web/Events/click)

// Действие по умолчанию

/*
Некоторые элементы в DOM обладают действиями по умолчанию. Например, если повесить обработчик на клик по 
ссылке, то выполнив этот клик, мы внезапно перейдем на другую страницу, ту которая была указана в href. 
Здесь мы видим пример того самого действия по умолчанию, на которое никак не влияет наличие обработчиков. 
Чтобы отменить это действие, а такое бывает нужно часто, необходимо вызвать метод event.preventDefault() 
внутри обработчика.
*/

<a href="#" id="myElement">Бум!</a>
const button = document.getElementById('myElement');

button.addEventListener('click', (e) => {
    e.preventDefault();
    alert(e.target.textContent);
});

// Иногда вы можете встретить такой код:

<a href="#" id="myElement" onclick="alert('hey'); return false;">Бум!</a>
// Возврат false внутри значения атрибута, приводит к такому же эффекту.

// Действиями по умолчанию обладают следующие элементы:

// Клик по ссылке приводит к переходу на страницу указанную в href атрибуте.
// Клик на кнопку с типом submit начинает отправку формы на сервер.
// Вращение колесом мышки в textarea передвигает текст если он не помещается
